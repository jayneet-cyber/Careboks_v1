const translations: Record<UiLanguage, Record<string, string>> = {
  eng: {},
  est,
  rus,
};

const interpolate = (text: string, variables?: Variables): string => {
  if (!variables) return text;

  return text.replace(/\{(.*?)\}/g, (_, key: string) => {
    const value = variables[key.trim()];
    return value === undefined ? `{${key}}` : String(value);
  });
};

const resolveInitialLanguage = (): UiLanguage => {
  const savedValue = localStorage.getItem(UI_LANGUAGE_STORAGE_KEY);
  return isUiLanguage(savedValue) ? savedValue : DEFAULT_UI_LANGUAGE;
};

const AppLanguageContext = createContext<LanguageContextValue | undefined>(undefined);

export const AppLanguageProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguageState] = useState<UiLanguage>(resolveInitialLanguage);

  const setLanguage = (nextLanguage: UiLanguage) => {
    setLanguageState(nextLanguage);
    localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, nextLanguage);
  };

  const t = (key: string, variables?: Variables) => {
    const translated = translations[language][key] ?? key;
    return interpolate(translated, variables);
  };

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      t,
    }),
    [language, t]
  );

  return <AppLanguageContext.Provider value={value}>{children}</AppLanguageContext.Provider>;
};

export const useAppLanguage = () => {
  const context = useContext(AppLanguageContext);
  if (!context) {
    throw new Error("useAppLanguage must be used within AppLanguageProvider");
  }

  return context;
};
